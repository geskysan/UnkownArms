#include "title.h"
#include "titleback.h"
#include "Input.h"
#include "fade.h"

//=============================================================================
// 初期化処理
//=============================================================================
void InitTitle(void)
{
	InitTitleback();
}
//=============================================================================
// 終了処理
//=============================================================================
void UninitTitle(void)
{
	UninitTitleback();
}
//=============================================================================
// 更新処理
//=============================================================================
void UpDateTitle(void)
{
	UpdateTitleback();
	// スペースキーで次のシーンへ
	if (GetKeyboardTrigger(DIK_SPACE))
		SetFade(MODE_GAMEMAIN);

	if (GetKeyboardTrigger(DIK_SPACE))
		SetMode(MODE_GAMEMAIN);
}
//=============================================================================
// 描画処理
//=============================================================================
void DrawTitle(void)
{
	DrawTitleback();
}